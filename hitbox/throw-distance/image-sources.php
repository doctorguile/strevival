function buildJson() {
    global $imagetable;
    $jsontable = array();
    foreach ($imagetable as $attacker => $subtable) {
        $jsontable[$attacker] = array();
        foreach ($subtable as $target => $movetable) {
            foreach ($movetable as $move => $path) {
                $relativepath = "images/$path";
                preg_match("/(\\d+).png$/", $path, $matches);
                $num                    = $matches[1];
                $jsontable[$attacker][] = array($relativepath, "$num ($move)");
            }
        }
    }
    echo "<script>App.Globals.table = " . json_encode($jsontable) . "</script>";
}